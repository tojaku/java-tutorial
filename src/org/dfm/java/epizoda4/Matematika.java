package org.dfm.java.epizoda4;

public class Matematika {

    public static void main(String[] args) {
        System.out.println("abs: " + Math.abs(-128));
        System.out.println("ceil: " + Math.ceil(15.25));
        System.out.println("floor: " + Math.floor(15.25));
        System.out.println("round: " + Math.round(15.25));
        System.out.println("min: " + Math.min(1, 2));
        System.out.println("max: " + Math.max(1, 2));
        System.out.println("log: " + Math.log(10));
        System.out.println("log10: " + Math.log10(100));
        System.out.println("sin: " + Math.sin(90));
        System.out.println("cos: " + Math.cos(90));
        System.out.println("pow: " + Math.pow(2, 10));
        System.out.println("sqrt: " + Math.sqrt(4));
        System.out.println("random: " + Math.random());
    }

}
